function buildMasterData(): MasterSection[] {
  const orderMap = new Map<string, number>();
  ITEMS_RAW.forEach(([itemNo], idx) => orderMap.set(itemNo, idx + 1));

  const itemsBySectionNo = new Map<number, MasterItem[]>();
  ITEMS_RAW.forEach(([itemNo, sectionNo, description, riskScore]) => {
    const id = itemNo.replace('.', '_');
    const item: MasterItem = {
      id,
      itemNo,
      description,
      riskScore,
      order: orderMap.get(itemNo)!,
    };
    if (!itemsBySectionNo.has(sectionNo)) itemsBySectionNo.set(sectionNo, []);
    itemsBySectionNo.get(sectionNo)!.push(item);
  });

  return SECTIONS_RAW.map((sec, idx) => ({
    id: `sec_${sec.no}`,
    no: sec.no,
    nameEn: sec.nameEn,
    order: idx + 1,
    items: itemsBySectionNo.get(sec.no) ?? [],
  }));
}

export const MASTER_SECTIONS: MasterSection[] = buildMasterData();

export const MASTER_ITEM_MAP: Map<string, MasterItem & { sectionNo: number }> = (() => {
  const map = new Map<string, MasterItem & { sectionNo: number }>();
  for (const section of MASTER_SECTIONS) {
    for (const item of section.items) {
      map.set(item.id, { ...item, sectionNo: section.no });
    }
  }
  return map;
})();